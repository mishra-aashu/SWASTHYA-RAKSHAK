-- Create a table for stocks
create table stocks (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default now(),
  medicine_name text,
  stock integer,
  min_stock integer,
  user_id uuid references auth.users
);

-- Set up RLS for stocks
alter table stocks
  enable row level security;

create policy "Kendra can view their own stocks." on stocks
  for select using (auth.uid() = user_id);

create policy "Kendra can insert stocks." on stocks
  for insert with check (auth.uid() = user_id and exists (select 1 from profiles where profiles.id = auth.uid() and profiles.role = 'kendra'));

create policy "Kendra can update their own stocks." on stocks
  for update using (auth.uid() = user_id and exists (select 1 from profiles where profiles.id = auth.uid() and profiles.role = 'kendra'));
